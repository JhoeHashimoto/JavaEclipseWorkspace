package Java_Initial;

public class RepeticaoWHILE {

	public static void main(String[] args) {
		
		
		/*   WHILE (enquanto)
		 * 	
		 * 
		 * while(condição lógica){
		 * 	
		 * 		instruções
		 * 
		 * }
		 * 
		 * 
		 * 
		 * 	int i =0;
		 * 
		 * while (i<=100){
		 * 
		 * 	System.out.println(i);
		 * 	i++;
		 * 
		 * }
		 * 	
		 * 
		 * 
		 */
		
	
		
		int i =0;
		
		while(i <=100) {
			System.out.println(i);
			i++;
		}

	}

}
